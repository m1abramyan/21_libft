#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;

	str = (unsigned char *)b;
	while (len > 0)
	{
		str[len - 1] = (unsigned char)c;
		len--;
	}
	return (b);
}
