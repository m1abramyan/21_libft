#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			ft_temp;
	unsigned int	nb;

	if (n < 0)
	{
		write(fd, "-", 1);
		nb = -n;
	}
	else
		nb = n;
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		ft_temp = nb + '0';
		write(fd, &ft_temp, 1);
	}
}
